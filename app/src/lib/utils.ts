export const generateRandomWords = (count: number, lang: string) => {
    let words: string[] = [];
    
    const selectedLanguage = languageData[lang as keyof typeof languageData];

    if (!selectedLanguage) 
        return [];

    while (words.length < count) {
        const rand = Math.floor(Math.random() * selectedLanguage.length);
        const randomWord = selectedLanguage[rand];

        if (!words.includes(randomWord)) {
            words.push(randomWord);
        }
        if (words.length >= selectedLanguage.length) 
            break;
    }

    return words;
}